package com.w3schools.examples;

public class CyklyWhileMain {
    public static void main(String[] args) {
        int i = 0;
        while(i<5){
            System.out.println(i);
            i++;
        }
        System.out.println();
        int y = 0;
        do{
            System.out.println(y);
            y++;
        }
        while(y<5);
    }
}
